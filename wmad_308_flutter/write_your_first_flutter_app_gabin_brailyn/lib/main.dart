//The MyApp class extends StatelessWidget. 
//Widgets are the elements from which you build every Flutter app. As you can see, even the app itself is a widget.
class MyApp extends StatelessWidget { //The code in MyApp sets up the whole app. It creates the app-wide state (more on this later), names the app, defines the visual theme, and sets the "home" widgetâ€”the starting point of your app.
  const MyApp({super.key});

  @override
  Widget build(BuildContext context) {
    return ChangeNotifierProvider(
      create: (context) => MyAppState(),
      child: MaterialApp(
        title: 'Namer App by Gabin,Brailyn',
        theme: ThemeData(
          useMaterial3: true,
          colorScheme: ColorScheme.fromSeed(seedColor: Colors.teal),
        ),
        home: MyHomePage(),
      ),
    );
  }
}

class MyAppState extends ChangeNotifier { //MyAppState defines the data the app needs to function.
  var current = WordPair.random(); 
//The state class extends ChangeNotifier, which means that it can notify others about its own changes. 
 //For example, if the current word pair changes, some widgets in the app need to know.
  
  void getNext(){ //The new getNext() method reassigns current with a new random WordPair.
    current = WordPair.random();
    notifyListeners();
    }

  var favorites = <WordPair>[];

  void toggleFavorite(){
    if (favorites.contains(current)){
      favorites.remove(current);
    }else {
      favorites.add(current);
    }
    notifyListeners();
  }
}

